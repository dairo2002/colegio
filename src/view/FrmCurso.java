    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!txtCodigo.getText().isEmpty() && !txtNombre.getText().isEmpty()) {
            String resultado = curso.agregarCurso(txtCodigo.getText(), txtNombre.getText());
            JOptionPane.showMessageDialog(null, resultado);
            limpiarDatos();
            cargarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (!txtCodigo.getText().isEmpty() && !txtNombre.getText().isEmpty()) {
            boolean resultado = curso.actualizarCurso(txtCodigo.getText(), txtNombre.getText());
            if (resultado) {
                JOptionPane.showMessageDialog(null, "Actualizacion exitosa");
                limpiarDatos();
                cargarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error no se realizo la actualizacion");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void tblaCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblaCursoMouseClicked
        int seleccionar = tblaCurso.rowAtPoint(evt.getPoint());
        txtCodigo.setText(String.valueOf(tblaCurso.getValueAt(seleccionar, 0)));
        txtNombre.setText(String.valueOf(tblaCurso.getValueAt(seleccionar, 1)));
    }//GEN-LAST:event_tblaCursoMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtCodigo.getText().isEmpty() && !txtNombre.getText().isEmpty()) {
            boolean resultado = curso.eliminarCurso(this.idCurso);
            if (resultado) {
                JOptionPane.showMessageDialog(null, "Actualizacion exitosa");
                limpiarDatos();
                cargarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error no se realizo la actualizacion");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

