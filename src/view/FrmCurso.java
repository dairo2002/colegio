    public void cargarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tblaCurso.getModel();
        modelo.setRowCount(0);
        cursosController cursoo = new cursosController();
        List<Cursos> lista = cursoo.listaCurso();
        lista.forEach((element) -> modelo.addRow(new Object[]{
            element.getCodigo(),
            element.getNombre()}));
    }

